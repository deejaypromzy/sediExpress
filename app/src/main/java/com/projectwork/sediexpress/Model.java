package com.projectwork.sediexpress;

public class Model {
    private String name;
    private String title;

    public Model() {
    }

    public Model(String name, String title) {
        this.name = name;
        this.title = title;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}
